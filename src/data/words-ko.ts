export const KOREAN_WORDS: Set<string> = new Set(allWords);

// 두음법칙 (Initial Sound Law) conversion map
export const DUEUM_MAP: Record<string, string> = {
  녀: "여",
  뇨: "요",
  뉴: "유",
  니: "이",
  랴: "야",
  려: "여",
  례: "예",
  료: "요",
  류: "유",
  리: "이",
  라: "나",
  래: "내",
  로: "노",
  뢰: "뇌",
  루: "누",
  르: "느",
};

/**
 * Apply 두음법칙 to a single character/syllable
 */
export function applyDueum(char: string): string {
  return DUEUM_MAP[char] ?? char;
}

/**
 * Check if a word exists in the dictionary
 */
export function isValidWord(word: string): boolean {
  return KOREAN_WORDS.has(word);
}

/**
 * Get all words starting with a given character,
 * also considering 두음법칙 variants
 */
export function getWordsStartingWith(char: string): string[] {
  const results: string[] = [];
  const dueumChar = applyDueum(char);

  for (const word of KOREAN_WORDS) {
    const firstChar = word[0];
    if (firstChar === char || firstChar === dueumChar) {
      results.push(word);
    }
  }

  return results;
}
