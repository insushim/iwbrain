// Helper functions
export function getPuzzlesBySize(size: number): NonogramPuzzleData[] {
  return NONOGRAM_PUZZLES.filter((p) => p.size === size);
}

export function getPuzzlesByCategory(category: string): NonogramPuzzleData[] {
  return NONOGRAM_PUZZLES.filter((p) => p.category === category);
}

export function getPuzzlesByDifficulty(
  difficulty: NonogramPuzzleData["difficulty"],
): NonogramPuzzleData[] {
  return NONOGRAM_PUZZLES.filter((p) => p.difficulty === difficulty);
}

export function getCategories(): string[] {
  return [...new Set(NONOGRAM_PUZZLES.map((p) => p.category))];
}

export function getPuzzleById(id: string): NonogramPuzzleData | undefined {
  return NONOGRAM_PUZZLES.find((p) => p.id === id);
}
